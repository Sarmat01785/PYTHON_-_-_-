# Распаковка списка по переменно.
z, x, c = [1, 2, 3]
print(z)
print(x)
print(c)
print(z, x, c)

z, x, *c = [1, 2, 3, 4, 5]
print(z)
print(x)
print(c)
print(z, x, c)

z, *x, c = [1, 2, 3, 4, 5]
print(z)
print(x)
print(c)
print(z, x, c)

*z, x, c = [1, 2, 3, 4, 5]  # * - arg
print(z)
