# Словари, тип данных dict()
# Часть 1. Варианты и методы
'''
Словари в Python - это тип данных, который позволяет хранить пары ключ-значение.
В отличие от последовательностей (например, списков или кортежей), словари не упорядочены, 
и доступ к значениям осуществляется по ключам.
'''

# Вот пример создания и использования словаря в Python:

# Создание словаря
student = {
    "имя": "Анна",
    "возраст": 20,
    "курс": "Программирование"
}

# Доступ к значениям по ключам
print(student["имя"])  # Выводит "Анна"
print(student["возраст"])  # Выводит 20

# Изменение значения по ключу
student["курс"] = "Искусственный интеллект"

# Добавление новой пары ключ-значение
student["группа"] = "A-32"

# Удаление пары ключ-значение
del student["возраст"]

# Проверка наличия ключа в словаре
if "курс" in student:
    print("Ключ 'курс' присутствует в словаре")

# Получение списка всех ключей и значений
keys = student.keys()
values = student.values()

# Перебор всех ключей и значений
for key, value in student.items():
    print(key, ":", value)
'''
В этом примере мы создаем словарь student, который содержит информацию о студенте: имя, возраст и курс. 
Мы можем получить доступ к значениям, используя их ключи, например student["имя"]. 
Мы также можем изменять значения по ключу, добавлять новые пары ключ-значение и удалять существующие ключи. 
Методы keys() и values() позволяют получить списки всех ключей и значений словаря соответственно. 
Метод items() позволяет получить пары ключ-значение словаря, которые мы можем перебрать с помощью цикла for.

Словари в Python очень удобны для хранения и организации данных, особенно когда 
у нас есть идентификаторы (ключи), по которым мы можем быстро получать доступ 
к нужным значениям. Они широко используются в различных задачах программирования 
и могут быть очень мощным инструментом.
'''





